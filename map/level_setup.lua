walls = {}

-- placehoder that reads from level 2d table
function setup_walls()
    for y = 1, 16 do
        for x = 1, 16 do
            if level[x][y] == 1 then
                add(walls, class_wall:new(tile_to_pixel(x - 1), tile_to_pixel(y - 1)), #walls+1)
            end
        end
    end
end

